package com.pojo;

public class Family {
	private int fid;
	private int aspectid;
	private String title;
	private String subtitle;
	private String sold_count;
	private String detail_text;
	private String product_pic;
	private String detail_pic;
	
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public int getAspectid() {
		return aspectid;
	}
	public void setAspectid(int aspectid) {
		this.aspectid = aspectid;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSubtitle() {
		return subtitle;
	}
	public void setSubtitle(String subtitle) {
		this.subtitle = subtitle;
	}
	public String getSold_count() {
		return sold_count;
	}
	public void setSold_count(String sold_count) {
		this.sold_count = sold_count;
	}
	public String getDetail_text() {
		return detail_text;
	}
	public void setDetail_text(String detail_text) {
		this.detail_text = detail_text;
	}
	public String getProduct_pic() {
		return product_pic;
	}
	public void setProduct_pic(String product_pic) {
		this.product_pic = product_pic;
	}
	public String getDetail_pic() {
		return detail_pic;
	}
	public void setDetail_pic(String detail_pic) {
		this.detail_pic = detail_pic;
	}
	
}
